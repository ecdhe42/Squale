LOOPCPY 
    lda ,U
*    cmpa #$C2               * Relocate any $C2xx pointer to $00xx
*    bne STRAIGHTCOPY
*    lda #$00
*STRAIGHTCOPY
    sta ,X+
    cmpx #END_ADR
    bne LOOPCPY

    jmp >$0000
